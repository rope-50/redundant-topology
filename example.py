def _sorted_nodes(graph):
    return sorted(graph, key=lambda n: (0, int(n)) if str(n).isdigit() else (1, str(n)))


def main() -> None:
    print("DWDM NETWORK GRAPH (example):")
    for node in _sorted_nodes(EXAMPLE_GRAPH):
        print(f"  {node}: {EXAMPLE_GRAPH[node]}")
    before = count_links(EXAMPLE_GRAPH)
    print(f"TOTAL: {before} fiber links\n")

    optimizer = DWDMNetworkOptimizer()
    optimized = optimizer.optimize(EXAMPLE_GRAPH)

    print("OPTIMIZED GRAPH:")
    for node in _sorted_nodes(optimized):
        print(f"  {node}: {optimized[node]}")
    after = count_links(optimized)
    print(f"TOTAL: {after} fiber links\n")

    reduction = 100.0 * (before - after) / before if before else 0.0
    print(f"Reduction: {before} -> {after} links ({reduction:.1f}% fewer)")


if __name__ == "__main__":
    main()
